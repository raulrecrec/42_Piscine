#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	int	x;

	x = 1;
	while (x != argc)
	{
		ft_putstr(argv[x]);
		write(1, "\n", 1);
		x++;
	}
}
